package workshop2;

public class Calculator {
	   public int add(int a, int b) {
	        return a + b;
	    }

	    // Add three integers
	    public int add(int a, int b, int c) {
	        return a + b + c;
	    }

	    // Add two doubles
	    public double add(double a, double b) {
	        return a + b;
	    }

	    // Add three doubles
	    public double add(double a, double b, double c) {
	        return a + b + c;
	    }
	}
